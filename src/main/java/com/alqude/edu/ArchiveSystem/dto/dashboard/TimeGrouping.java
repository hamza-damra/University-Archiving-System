package com.alqude.edu.ArchiveSystem.dto.dashboard;

/**
 * Enum for time grouping options in chart data queries.
 */
public enum TimeGrouping {
    /**
     * Group data by day
     */
    DAY,
    
    /**
     * Group data by week
     */
    WEEK,
    
    /**
     * Group data by month
     */
    MONTH
}
